    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String telefono = txtTelefono.getText();
        String correo = txtCorreo.getText();

        // Verificar si el teléfono ya está registrado
        if (telefonosRegistrados.contains(telefono)) {
            JOptionPane.showMessageDialog(this, "El número de teléfono ya está registrado.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Crear una cadena con los datos del contacto
        String contacto = "Nombre: " + nombre + "\n" +
                          "Teléfono: " + telefono + "\n" +
                          "Correo: " + correo + "\n\n";

        // Escribir los datos en el archivo 'contactos.txt'
        try (FileWriter fw = new FileWriter("contactos.txt", true)) {
            fw.write(contacto);
            telefonosRegistrados.add(telefono); // Añadir el teléfono al conjunto
            System.out.println("Contacto registrado correctamente en 'contactos.txt'");
        } catch (IOException ex) {
            System.err.println("Error al escribir en el archivo 'contactos.txt': " + ex.getMessage());
        }

        // Limpiar los campos de texto
        txtNombre.setText("");
        txtTelefono.setText("");
        txtCorreo.setText("");
    
    }//GEN-LAST:event_btnRegistrarActionPerformed


    private void cargarTelefonosRegistrados() {
        try (BufferedReader br = new BufferedReader(new FileReader("contactos.txt"))) {
            String linea;
            while ((linea = br.readLine()) != null) {
                if (linea.startsWith("Teléfono: ")) {
                    String telefono = linea.substring(10).trim();
                    telefonosRegistrados.add(telefono);
                }
            }
        } catch (IOException e) {
            System.out.println("Error al leer el archivo 'contactos.txt': " + e.getMessage());
        }
    }

